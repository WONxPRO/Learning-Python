# 4 Types of variables; str, int, bool, float

# We can declare variables in Python by simply assigning a value to a variable name. 
# The type of the variable is determined by the value assigned to it.
name = "Nathan Suwiji"  # String variable
age = 17                # Integer variable
is_student = False      # Boolean variable (True or False)
height = 1.79           # Float variable (decimal number)

# Updating the variables
name = "Nathan"        # Updating the string variable
age = 18               # Updating the integer variable
is_student = True      # Updating the boolean variable
height = 1.80          # Updating the float variable

# Printing the variables using the print() function
print(name)
print(age)
print(is_student)
print(height)

# Printing the variables using f-strings for better formatting
print(f"Name: {name}")
print(f"Age: {age}")
print(f"Is Student: {is_student}")
print(f"Height: {height}")