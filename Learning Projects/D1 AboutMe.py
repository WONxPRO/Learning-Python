# This is a simple program that prints out some information about me.

Name = "Nathan Suwiji"              # Declaration of a variable called Name and assigning it the value "Nathan Suwiji" (string data type)
Country = "Indonesia"               # Declaration of a variable called Country and assigning it the value "Indonesia" (string data type)
FutureMajor = "Computer Science"    # Declaration of a variable called FutureMajor and assigning it the value "Computer Science" (string data type)
FavoriteNumber = 2 # Declaration of a variable called FavoriteNumber and assigning it the value 2 (integer data type)

# Printing out the information using print() function
print("Name:", Name)                        # 
print("Country:", Country)                  #
print("Future Major:", FutureMajor)         #
print("Favorite Number:", FavoriteNumber)   #

# Printing out the information using f-strings for better formatting
print(f"\nName: {Name}")
print(f"Country: {Country}")
print(f"Future Major: {FutureMajor}")
print(f"Favorite Number: {FavoriteNumber}")

# Both works the same, but f-strings allow for easier readability and formatting.