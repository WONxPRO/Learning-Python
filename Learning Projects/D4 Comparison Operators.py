num1 = input("Enter first number: ")

print(f"First number is smaller than 20: {int(num1) < 20}")
print(f"First number is greater than 20: {int(num1) > 20}")
print(f"First number is equal to 20: {int(num1) == 20}")
print(f"First number is not equal to 20: {int(num1) != 20}")
print(f"First number is smaller than or equal to 20: {int(num1) <= 20}")
print(f"First number is greater than or equal to 20: {int(num1) >= 20}")